USE multive8_cards;

DROP TABLE IF EXISTS orderItems;
DROP TABLE IF EXISTS orderInfo;
DROP TABLE IF EXISTS users;
DROP TABLE IF EXISTS cart;
DROP TABLE IF EXISTS cards;


CREATE TABLE cards(
  card_num        INT(3)     NOT NULL,
  card_name       VARCHAR(100)    NOT NULL,
  card_color	  VARCHAR(12)	  NOT NULL,
  card_cost		  INT(10)		  NOT NULL,
  card_type		  VARCHAR(30)	  NOT NULL,
  card_rarity	  CHAR(1)		  NOT NULL,
  price		      FLOAT(4)       NOT NULL,
  qty		      INT(3)         NOT NULL,
  PRIMARY KEY (card_num)
);


CREATE TABLE cart (
  card_ID        INT(3)     NOT NULL,
  quantity  	  INT(15)		  NOT NULL,
  PRIMARY KEY (card_ID),
  CONSTRAINT FOREIGN KEY (card_ID) REFERENCES cards(card_num)
);

CREATE TABLE users (
	user_name	VARCHAR(12)		NOT NULL,
	user_pass	VARCHAR(255)	NOT NULL,
	user_type	CHAR(1)			DEFAULT 'C',
	user_email	VARCHAR(40),
	PRIMARY KEY (user_name)
);

CREATE TABLE orderInfo (
   order_num	INT(10)              NOT NULL,
   order_date	DATETIME             DEFAULT CURRENT_TIMESTAMP,
   user_ID	VARCHAR(40)          NOT NULL,
   PRIMARY KEY (order_num),
   CONSTRAINT FOREIGN KEY (user_ID) REFERENCES users(user_name)
);

CREATE TABLE orderItems (
   order_num	INT(10)              NOT NULL ,
   card_ID		INT(3)          	 NOT NULL,
   quantity		INT(15)              NOT NULL,
   PRIMARY KEY (order_num, card_ID),
   CONSTRAINT FOREIGN KEY (order_num) REFERENCES orderInfo(order_num),
   CONSTRAINT FOREIGN KEY (card_ID) REFERENCES cards(card_num)
);

